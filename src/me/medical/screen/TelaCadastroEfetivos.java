    //-------------------------------------------metodo listar,cadastrar,alterar,excluir--------------------------
    public void listarMedicos() {
        Conexao conec = new Conexao();
        DefaultTableModel model = (DefaultTableModel) jTableMedicos.getModel();
        model.setNumRows(0);
        String sql = "Select codigo,NomeServidor,EspecialidadeServidor,Registro,Uf from tb_servidor order by codigo";
        try {
            pst = Conexao.conector().prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("codigo"),
                    rs.getString("NomeServidor"),
                    //Utilitarios.Utils.convertData(rs.getDate("DataConsulta")),
                    rs.getString("EspecialidadeServidor"),
                    rs.getString("Registro"),
                    rs.getString("Uf"),
                });
            }
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);
        }finally {
            try {
                rs.close();
                pst.close();
                conec.desconectar();
            } catch (Exception ex) {
            }
        }

    }

    private void cadastrarMedicos() {
        Conexao conec = new Conexao();
        String sql = "Insert into tb_servidor (NomeServidor,EspecialidadeServidor,Registro,Uf )values(?,?,?,?)";

        try {
            pst = Conexao.conector().prepareStatement(sql);

            pst.setString(1, txtNomeMedico.getText());
            pst.setString(2, jcEspecialidades.getSelectedItem().toString());
            pst.setString(3, txtRegistro.getText());
            pst.setString(4, JcUFRegistro.getSelectedItem().toString());
            pst.execute();
            JOptionPane.showMessageDialog(rootPane, " Profissional " + txtNomeMedico.getText().toUpperCase() + " salvo com sucesso.", "Inclusão", JOptionPane.INFORMATION_MESSAGE,icon);
            listarMedicos();

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);
        }finally {
            try {
                rs.close();
                pst.close();
                conec.desconectar();
            } catch (Exception ex) {
            }
        }
    }

    private void deletarMedicos() {
        Conexao conec = new Conexao();
        String sql = "Delete from tb_servidor where codigo = ?";
        try {
            pst = Conexao.conector().prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(txtId.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(rootPane, " Profissional " + txtNomeMedico.getText() + " Excluido com sucesso.", "Exclusão", JOptionPane.INFORMATION_MESSAGE,iconExcluir);
            listarMedicos();
        } catch (SQLException error) {

            JOptionPane.showMessageDialog(null, error);
        }finally {
            try {
                rs.close();
                pst.close();
                conec.desconectar();
            } catch (Exception ex) {
            }
        }
    }

    private void editarMedicos() {
        Conexao conec = new Conexao();
        String sql = "Update tb_servidor set  NomeServidor=?,EspecialidadeServidor=?,Registro=?,Uf=? where codigo = ? ";

        try {
            pst = Conexao.conector().prepareStatement(sql);
            pst.setString(1, txtNomeMedico.getText());
            pst.setString(2, jcEspecialidades.getSelectedItem().toString());
            pst.setString(3, txtRegistro.getText());
            pst.setString(4, JcUFRegistro.getSelectedItem().toString());
            pst.setInt(5, Integer.parseInt(txtId.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, " Profissional " + txtNomeMedico.getText() + " Editado com sucesso.", "Alteração", JOptionPane.INFORMATION_MESSAGE,icon);
            listarMedicos();

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);
        }finally {
            try {
                rs.close();
                pst.close();
                conec.desconectar();
            } catch (Exception ex) {
            }
        }
    }

