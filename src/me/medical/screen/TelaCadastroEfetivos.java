    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (txtNomeMedico.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Campo Nome do Servidor é obrigatório", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtNomeMedico.requestFocus();
            return;
        }

        if (jcEspecialidades.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Campo Especialidades é Obrigatório", "Atenção", JOptionPane.WARNING_MESSAGE);
            jcEspecialidades.requestFocus();
            return;
        }

        if (txtRegistro.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Campo Registro é Obrigatório", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtRegistro.requestFocus();
            return;
        }

        if (JcUFRegistro.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Campo UF Registro é Obrigatório", "Atenção", JOptionPane.WARNING_MESSAGE);
            JcUFRegistro.requestFocus();
            return;
        }


        cadastrarMedicos();
        limparCampos();
        desaabilitaCampos();
        btnSalvar.setEnabled(false);
        btnNovo.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int i = jTableMedicos.getSelectedRow();
        if (i < 0) {
            Msg.alerta(this, "Selecione um Registro!");
            return;
        }
        deletarMedicos();
        desaabilitaCampos();
        limparCampos();
        btnNovo.setEnabled(true);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
    }//GEN-LAST:event_btnExcluirActionPerformed

