    private void alterar() {
        //chama a tela ja com os dados
        //tela = new TelaInternacao(this);
        // chama a tela ja com os dados
        int index = jTableSolicitacao.getSelectedRow();
        TableModel model = jTableSolicitacao.getModel();
        String Codigo = model.getValueAt(index, 0).toString();
        String DataPedido = model.getValueAt(index, 1).toString();
        String Setor = model.getValueAt(index, 2).toString();
        String TipoMedicacao = model.getValueAt(index, 3).toString();
        String Solicitante = model.getValueAt(index, 4).toString();
        String StatusEntrega = model.getValueAt(index, 5).toString();
        tela.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        tela.txtCodigo.setText(Codigo);
        tela.txtDataPedido.setText(DataPedido);
        tela.JcSetorPedido.setText(Setor);
        tela.JcPedidoMedicacao.setText(TipoMedicacao);
        tela.txtSolicitante.setText(Solicitante);
        tela.JcStatus.setSelectedItem(StatusEntrega);

        //tela.txtDataEntrega.getEditor().setText(DataEntrega);
        tela.setVisible(true);
        tela.btnSalvar.setEnabled(true);
        tela.pack();
    }

    private void excluir(int id) {
        Conexao conec = new Conexao();
        String sql = "DELETE FROM `consultorio`.`tb_solicitacao` WHERE  `codigo`=" + id + ";";

        try {
            pst = Conexao.conector().prepareStatement(sql);
            pst.executeUpdate();
            rs.close();
            pst.close();
            conec.desconectar();
            listarSolicitacao();
            Msg.informacao(this, "Excluido com Sucesso!");
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, error);
        }
    }


