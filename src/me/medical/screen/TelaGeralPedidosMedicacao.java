    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int i = jTableSolicitacao.getSelectedRow();
        if (i < 0) {
            Msg.alerta(this, "Selecione um Registro!");
            jTableSolicitacao.setEnabled(true);
            return;
        } else {
            if (Msg.confirmar(this, "Deseja Realmente Excluir Este Registro?")) {
                excluir(Integer.parseInt(jTableSolicitacao.getValueAt(i, 0).toString()));
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

