    private void iniciar() {
        ini = new IniFiles("config.ini");
        this.preencheCampos();
    }

    private void preencheCampos() {
        try {
            cbTema.setSelectedItem(ini.getString("APARENCIA", "tema"));
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "Erro ao carregaros dados!");
        }
    }

    private void salvar() {
        try {
            ini.setString("APARENCIA", "tema", cbTema.getSelectedItem().toString());
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "Erro ao Salvar as Configurações!");
        }
    }

