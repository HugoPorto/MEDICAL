    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        alterarSolicitacao();
//        desaabilitaCampos();
        limparCampos();
        btnSalvar.setEnabled(false);
       

            // gerando um relatorio de clientes
        int confirma = JOptionPane.showConfirmDialog(null, "DESEJA IMPRMIR A OS de entrega?", "Atenção", JOptionPane        .YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {

            final me.medical.screen.Aguarde carregando = new me.medical.screen.Aguarde();
            carregando.setVisible(true);
            Thread t = new Thread() {
                public void run() {

                    try {
                        // metodos que chama o relatorio por parametro
                        String parametro = JOptionPane.showInputDialog(" Digite abaixo o CÓDIGO da OS de entrega");
                        Map parametros = new HashMap();
                        parametros.put("codigo", parametro);

                        // Usando a classe JasperPrint para preparar a impressão de um relatorio
                        JasperPrint print = JasperFillManager.fillReport("C:/RelatorioConsultorio/OSmedicacao.jasper", parametros, conexao);
                        // a linha abaixo exibe o relatorio atraves da classe JasperViewer
                        JasperViewer.viewReport(print, false);
                        carregando.dispose();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }
            };
            t.start();
         } else{
        
            dispose(); 
        }
    }//GEN-LAST:event_btnSalvarActionPerformed
        
