     public void alterarSolicitacao() {
        Conexao conec = new Conexao();
        String sql = "update tb_Solicitacao set StatusEntrega=? where codigo=?";

        try {
            pst = Conexao.conector().prepareStatement(sql);
            pst.setString(1, JcStatus.getSelectedItem().toString());
            pst.setInt(2, Integer.parseInt(txtCodigo.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, " Solicitação do medicamento " + JcPedidoMedicacao.getText()+ " Alterado com sucesso.", "Alteração", JOptionPane.INFORMATION_MESSAGE, icon);
            dispose();
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);
        } finally {
            try {
                rs.close();
                pst.close();
                conec.desconectar();
            } catch (Exception ex) {
            }
        }
    }
     
      public void removerSolicitacao() {
        Conexao conec = new Conexao();
        String sql = "delete from tb_Solicitacao where codigo=?";

        try {
            pst = Conexao.conector().prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(txtCodigo.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(rootPane, " Solicitação do medicamento " + JcPedidoMedicacao.getText()+ " Excluido com sucesso.", "Exclusão", JOptionPane.INFORMATION_MESSAGE, iconExcluir);
        } catch (SQLException error) {

            JOptionPane.showMessageDialog(null, error);
        }finally {
            try {
                rs.close();
                pst.close();
                conec.desconectar();
            } catch (Exception ex) {
            }
        }
    }
      
     
