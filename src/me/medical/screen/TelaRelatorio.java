    private void btnRelatorioPacientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelatorioPacientesActionPerformed
       
            //imprimindo relatorio com o framework JaspReport
            final Aguarde carregando = new Aguarde();
            carregando.setVisible(true);
            Thread t = new Thread(){
                public void run(){

                    try {
                        // Usando a classe JasperPrint para preparar a impressão de um relatorio
                        JasperPrint print = JasperFillManager.fillReport("C:/RelatorioConsultorio/Clientes.jasper", null, conexao);
                        // a linha abaixo exibe o relatorio atraves da classe JasperViewer
                        JasperViewer.viewReport(print, false);
                        carregando.dispose();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }
            };
            t.start();

        
                                                      
    }//GEN-LAST:event_btnRelatorioPacientesActionPerformed

