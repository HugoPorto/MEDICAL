    private void btnRelatorioLaudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelatorioLaudoActionPerformed
      
        
            final Aguarde carregando = new Aguarde();
            carregando.setVisible(true);
            Thread t = new Thread(){
                public void run(){

                    try {
                        // metodos que chama o relatorio por parametro
                        String parametro = JOptionPane.showInputDialog(" Digite o Nome do Paciente");
                        Map parametros = new HashMap();
                        parametros.put("NomePaciente", parametro);

                        // Usando a classe JasperPrint para preparar a impressão de um relatorio
                        JasperPrint print = JasperFillManager.fillReport("C:/RelatorioConsultorio/Laudo.jasper", parametros, conexao);
                        // a linha abaixo exibe o relatorio atraves da classe JasperViewer
                        JasperViewer.viewReport(print, false);
                        carregando.dispose();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                    dispose();
                }
            };
            t.start();
                   
    }//GEN-LAST:event_btnRelatorioLaudoActionPerformed

